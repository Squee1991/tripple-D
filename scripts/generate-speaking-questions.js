// Build the expanded JSON
const result = { ...existing }

const topics = Object.keys(topicQuestions)
const levels = ['A1', 'A2', 'B1', 'B2', 'C1']

for (const topic of topics) {
  for (const level of levels) {
    const bankId = `${topic}-${level}`
    const questions = topicQuestions[topic][level].map((text, i) => ({
      id: `${topic}-${level.toLowerCase()}-${String(i + 1).padStart(2, '0')}`,
      text,
      category: topic
    }))
    result[bankId] = {
      cefrLevel: level,
      category: topic,
      questions
    }
  }
}

const outPath = join(__dirname, '..', 'server', 'assets', 'speaking-questions', 'questions.json')
writeFileSync(outPath, JSON.stringify(result, null, 2) + '\n')

